import { AfterViewInit, Component, Inject } from '@angular/core';
import { FormBuilder, FormControl, FormGroup, Validators } from '@angular/forms';
import { MAT_DIALOG_DATA, MatDialog } from '@angular/material/dialog';
import { ActivatedRoute, Router } from '@angular/router';
import { PageMasterService } from 'app/modules/services/page-master.service';
import { ToastrService } from 'ngx-toastr';
import { Observable } from 'rxjs';
import { map, startWith } from 'rxjs/operators';

@Component({
    selector: 'app-criacao-grupo',
    templateUrl: './edicao-criacao-grupo.component.html',
    styleUrls: ['./edicao-criacao-grupo.component.scss'],
})
export class EdicaoCriacaoGrupoComponent {
    state = 'Criar'
    constructor(
        private router: Router,
        public dialog: MatDialog,
        @Inject(MAT_DIALOG_DATA) public data,
        public fb: FormBuilder,
        private toastr: ToastrService,
        private pageMasterService: PageMasterService
    ) {
        this.requisicoes();
    }
    clearSelection() {
        this.form.patchValue({
            icon: '',
            search: ''
        })
        this.form.controls.icon.reset()
        this.form.controls.search.reset()
        this.requisicoes()
    }
    showAll = false;
    showAllFunc() {
        this.showAll = !this.showAll;
        this.requisicoes()
    }
    _filter(value: string): any[] {
        const sliceValue = this.showAll ? 3000 : 100;
        return mat_outline
            .filter((option) => option.icon.toLowerCase().includes(value))
            .slice(0, sliceValue);
    }
    selectIcon(opt) {
        this.form.patchValue({
            icon: opt.type + ':' + opt.icon,
            search: opt.icon,
        });
    }
    async requisicoes() {
        this.filteredOptions = this.form.controls.search.valueChanges.pipe(
            startWith(''),
            map((value) => this._filter(value || ''))
        );
    }
    form = this.fb.group({
        id: [''],
        title: ['', Validators.required],
        icon: ['', Validators.required],
        search: ['', Validators.required],
    });
    mat_outline = [];
    feather = mat_outline;
    filteredOptions: Observable<any[]>;
    iconsTypeList = ['mat_outline', 'feather'];
    iconType = 'mat_outline';

    criarGrupo(): void {
        if (!this.form.valid) {
            this.toastr.error('Nome Precisa ser preenchido');
            return;
        }
        delete this.form.value.search
        delete this.form.value.id
        this.pageMasterService.postGroup(this.form.value).subscribe(
            (res) => {
                this.dialog.closeAll();
                this.toastr.success('Grupo criado com sucesso');
                // this.router.navigate(['/master/gestao/telas']);
                this.data.data();
            },
            ({ error }) => {
                this.toastr.error('Falha ao criar grupo');
            }
        );
    }
}

@Component({
    selector: 'app-edicao-grupo',
    templateUrl: './edicao-criacao-grupo.component.html',
    styleUrls: ['./edicao-criacao-grupo.component.scss'],
})
export class EdicaoGrupoComponent extends EdicaoCriacaoGrupoComponent {
    state = 'Editar'
    constructor(
          router: Router,
          dialog: MatDialog,
        @Inject(MAT_DIALOG_DATA) public data,
        public _fb: FormBuilder,
        private _toastr: ToastrService,
        public _pageMasterService: PageMasterService
    ) {
        super(router, dialog,data,_fb,_toastr,_pageMasterService)
        this.form.patchValue({ id: this.data.data.form.id,title: this.data.data.form.name, icon: this.data.data.form.icon,search: this.data.data.form.icon.split(':')[1]})
        this.filteredOptions = this.form.controls.search.valueChanges.pipe(
            startWith(''),
            map((value) => this._filter(value))
        );
    }
    clearSelection() {
        this.form.patchValue({
            icon: '',
            search: ''
        })
        this.form.controls.icon.reset()
        this.form.controls.search.reset()
        this.filteredOptions = this.form.controls.search.valueChanges.pipe(
            startWith(''),
            map((value) => this._filter(value))
        );

    }
  
    form = this._fb.group({
        id: ['', Validators.required],
        title: ['', Validators.required],
        icon: ['', Validators.required],
        search: ['', Validators.required],
    });


    criarGrupo(): void {
        if (!this.form.valid) {
            this._toastr.error('Nome do grupo precisa ser preenchido');
            return;
        }
        this._pageMasterService
            .updateGroup(this.form.value.id, {
                id: this.form.value.id,
                title: this.form.value.title,
                icon: this.form.value.icon,
            })
            .subscribe(
                (res) => {
                    this.dialog.closeAll()
                    this._toastr.success('Grupo Atualizado');
                },
                ({ error }) => {
                    this._toastr.error('Falha ao atualizar grupo');
                }
            );
    }

}
